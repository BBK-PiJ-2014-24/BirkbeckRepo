
public class Rectangle {
	
	// Instance Variables
	// ------------------
	
	private Point upLeft; 
	private Point downRight;
	
	// Access Methods
	// --------------
	
	public void setUpLeft(Point p){
		upLeft = p;
	}
	
	public Point getUpLeft(){
		return upLeft;
	}
	
	public void setDownRight(Point p){
		downRight = p;
	}
	
	public Point getDownRight(){
		return downRight;
	}
	
	

}
